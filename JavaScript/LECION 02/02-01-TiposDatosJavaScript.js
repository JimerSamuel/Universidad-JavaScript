/*
Ejemplos de 
Tipos de datos
en JavaScript
*/

//Tipo de dato string
var nombre = "Samuel";
console.log(nombre);

//Ver el tipo de dato de una variable
nombre = 10;
console.log(typeof nombre);

//Tipo de dato number

var numero = 1000;
console.log(numero);

//Tipo de dato Objeto
var objeto = {
  nombre: "Jimer",
  apellido: "Espinoza",
  edad: 23,
  telefono: "123456789",
};
console.log(objeto);
console.log(typeof objeto);
